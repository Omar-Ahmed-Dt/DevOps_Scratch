# True Values
print(bool("Omar"))
print(bool(100))
print(bool(True))
print(bool([1, 2, 3]))

print("=" * 50)
# False Values
print(bool(0))
print(bool(""))
print(bool(''))
print(bool(False))
print(bool([]))
print(bool(()))
print(bool({}))
