# ----------------------------------
# --------Boolean Operators---------
# ----------------------------------

# [1] and
age = 24
country = "Egypt"

print(age > 15)
print(country == "Egypt")

print("=" * 50)
# or :
print(age > 15 and country == "Egypt")

print("=" * 50)
# [2] or
name = "Omar"
age = 24
country = "Egypt"

print(age > 15 or country == "Russia" or name == "Ali")

print("=" * 50)

# [3] not
print(not name == "Ahmed")
print(not age == 24)
