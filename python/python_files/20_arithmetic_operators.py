# --------------------------
# -- Arithmetic Operators --
# --------------------------
# [+] Addition
# [-] Subtraction
# [*] Multiplication
# [/] Division
# [%] Modulus
# [**] Exponent
# [//] Floor Division
# --------------------------

# Addition
print(10 + 30)
print(-10+80)
print(3.4+1)

# Subtraction
print(80 - 30)
print(4-80)

# Multiplication
print(3*8)
print(8+4*4)
print((5+10)*100)

# Division
print(20/2)
print(int(20/2))
print(complex(20/2))
print(2/4)
print(int(2/4))

# Modulus
print(8 % 2)
print(5 % 2)
print(8 % 3)

# Exponent
print(5 ** 2)
print(2 ** 5)

# Floor Division
print(20 // 2)
print(21 // 2)
print(9 // 3)
print(10 // 3)
